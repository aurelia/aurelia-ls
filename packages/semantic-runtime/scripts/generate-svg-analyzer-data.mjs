import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(here, '../../..');
const sourcePath = resolve(repoRoot, 'aurelia/packages/runtime-html/src/observation/svg-analyzer.ts');
const outputPath = resolve(repoRoot, 'packages/semantic-runtime/src/observation/svg-analyzer-data.generated.ts');

const source = readFileSync(sourcePath, 'utf8');
const elementsBlock = between(
  source,
  'private readonly _svgElements',
  'private readonly _svgPresentationElements',
);

const elementAttributes = new Map();
const elementEntryPattern = /'([^']+)':\s*(?:o\('([^']*)'\)|createLookup<[^>]+>\(\))/g;
for (const match of elementsBlock.matchAll(elementEntryPattern)) {
  const elementName = normalize(match[1]);
  const attrs = attributeSetFor(elementAttributes, elementName);
  for (const attr of words(match[2] ?? '')) {
    attrs.add(normalize(attr));
  }
}

const presentationElements = new Set(
  words(extractOCall(source, 'private readonly _svgPresentationElements')).map(normalize),
);
const presentationAttributes = new Set(
  words(extractOCall(source, 'private readonly _svgPresentationAttributes')).map(normalize),
);

const generated = `// @generated by packages/semantic-runtime/scripts/generate-svg-analyzer-data.mjs
// Source of truth: aurelia/packages/runtime-html/src/observation/svg-analyzer.ts
// Do not edit by hand.

const svgElementAttributes = ${objectLiteralFromMap(elementAttributes)} as const;

const svgPresentationElements = ${arrayLiteral(presentationElements)} as const;

const svgPresentationAttributes = ${arrayLiteral(presentationAttributes)} as const;

const svgElementAttributeLookup = new Map<string, ReadonlySet<string>>(
  Object.entries(svgElementAttributes).map(([elementName, attributes]) => [elementName, new Set(attributes)]),
);
const svgPresentationElementLookup = new Set<string>(svgPresentationElements);
const svgPresentationAttributeLookup = new Set<string>(svgPresentationAttributes);

export function isKnownSvgElement(tagName: string): boolean {
  return svgElementAttributeLookup.has(normalizeSvgKey(tagName));
}

export function isStandardSvgAttribute(tagName: string, attributeName: string): boolean {
  const normalizedTagName = normalizeSvgKey(tagName);
  const normalizedAttributeName = normalizeSvgKey(attributeName);
  return (
    svgPresentationElementLookup.has(normalizedTagName) && svgPresentationAttributeLookup.has(normalizedAttributeName)
  ) || (
    svgElementAttributeLookup.get(normalizedTagName)?.has(normalizedAttributeName) === true
  );
}

function normalizeSvgKey(value: string): string {
  return value.toLowerCase();
}
`;

writeFileSync(outputPath, generated);

function between(value, start, end) {
  const startIndex = value.indexOf(start);
  const endIndex = value.indexOf(end, startIndex);
  if (startIndex < 0 || endIndex < 0) {
    throw new Error(`Could not find source block between ${start} and ${end}`);
  }
  return value.slice(startIndex, endIndex);
}

function extractOCall(value, marker) {
  const startIndex = value.indexOf(marker);
  if (startIndex < 0) {
    throw new Error(`Could not find ${marker}`);
  }
  const rest = value.slice(startIndex);
  const match = /o\('([^']*)'\)/.exec(rest);
  if (match == null) {
    throw new Error(`Could not find o('...') call after ${marker}`);
  }
  return match[1];
}

function words(value) {
  return value.split(/\s+/).filter(Boolean);
}

function normalize(value) {
  return value.toLowerCase();
}

function attributeSetFor(map, elementName) {
  let attrs = map.get(elementName);
  if (attrs == null) {
    attrs = new Set();
    map.set(elementName, attrs);
  }
  return attrs;
}

function objectLiteralFromMap(map) {
  const entries = [...map.entries()]
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([elementName, attrs]) => `  ${JSON.stringify(elementName)}: ${arrayLiteral(attrs)}`);
  return `{\n${entries.join(',\n')}\n}`;
}

function arrayLiteral(values) {
  const sorted = [...values].sort((a, b) => a.localeCompare(b));
  return `[${sorted.map((value) => JSON.stringify(value)).join(', ')}]`;
}
