/** Compiler-front-door product: source + world + inquiry pressure + root context. */
export class TemplateCompilationUnit {
  constructor(
    /** Product handle for the materialized-product envelope that represents this unit. */
    readonly productHandle: ProductHandle,
    /** Identity for this compilation unit. */
    readonly identityHandle: IdentityHandle,
    /** Compiler request lane. */
    readonly unitKind: TemplateCompilationUnitKind,
    /** Authored source that will be parsed/compiled. */
    readonly templateSource: TemplateSourceReference,
    /** Compiler world selected by configuration and DI world construction. */
    readonly compilerWorld: TemplateCompilerWorldReference,
    /** Inquiry pressure shared by parser and lowering passes. */
    readonly parseContext: TemplateParseContextReference,
    /** Root runtime-shaped compilation context for this unit. */
    readonly rootContext: TemplateCompilationContextReference,
    /** Source address for the unit owner. */
    readonly sourceAddressHandle: AddressHandle | null,
    /** Field-level provenance for source facts that matter to explanation or ambiguity. */
    readonly fieldProvenance: readonly FieldProvenance<TemplateCompilationUnitField>[] = [],
  ) {}

  toReference(): TemplateCompilationUnitReference {
    return new TemplateCompilationUnitReference(
      this.productHandle,
      this.identityHandle,
      this.unitKind,
      this.sourceAddressHandle,
    );
  }
}
